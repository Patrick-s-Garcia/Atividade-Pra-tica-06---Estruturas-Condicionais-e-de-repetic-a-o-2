let dia = Number(prompt("Digite um numero de 1 a 7"))





if(dia == 1){
    console.log("Domingo")
}
else if(dia == 2){
    console.log("segunda")
}
else if(dia == 3){
    console.log("terça")
}
else if(dia == 4){
    console.log("quarta")
}
else if(dia == 5){
    console.log("quinta")
}
else if(dia == 6){
    console.log("sexta")
}
else if(dia == 7){
    console.log("sabado")
}
else{
    console.log("Dia não reconhecido")
}